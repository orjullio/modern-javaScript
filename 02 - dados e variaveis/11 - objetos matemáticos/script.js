let x;

//raiz quadradada
x = Math.sqrt(9);
console.log(x)

//número absoluto
x = Math.abs(-5);
console.log(x)

//arredondar número
x = Math.round(56.89);
console.log(x);

//"ceil" arrendodar pra cima
x = Math.ceil(4.23);
console.log(x);

//floor - arrendonda para baixo
x = Math.floor(5.98);
console.log(x);

//pow - quantas vezes o primeiro número deve ser multiplicado por ele mesmo
x = Math.pow(4, 4);
console.log(x);

//número mínimo entre dos selecionados
x = Math.min(2, 6);
console.log(x);

//número máximo entre selecionados
x = Math.max(2, 4, 0.8, 5);
console.log(x);

//número aleatório
x = Math.random();
console.log(x);

x = Math.random() * 10 + 1;
console.log(x);

// número randomico entre 1 e 10
x = Math.floor(Math.random() * 10 + 1);
console.log(x);